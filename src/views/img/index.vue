      <h3>南山区教育局法治建设工作报告</h3>
      <span>2020-08-26 17:50来源</span>
      <i>深圳市南山区教育局</i>
      <div>
        <p>
          “七五”普法工作开展以来,南山区教育局自觉强化使命担当，抢抓粤港澳大湾区和先行示范区“双区驱动”战略机遇，根据“南山区全面依法治区工作要点”要求，按照“坚守教育初心，落实立德树人，推进南山教育高质量发展”的工作思路，大力推进南山教育法治建设工作，努力构建南山教育法治体系，着力营造和谐安全校园环境，全力打造教育的“南山质量”，荣获区委嘉奖令，获评深圳年度基层民生服务工作创新奖、最受欢迎民生实事，在南山“世界级创新型滨海中心城区”建设中，体现了教育担当，贡献了教育智慧和力量。
        </p>

        <p>
          一、主要工作
        </p>

        <p>
          （一）强化领导，提高教育依法行政能力
        </p>

        <p>
          一是高度重视法治政府建设工作。成立以刘根平局长为组长，其他局领导班子成员为副组长，局机关各科室负责人为成员的领导小组，并明确专人负责此项工作，形成层层有人抓、层层有人管、层层有人负责的工作格局，提高法治政府建设工作效率和工作质量，把法治政府建设落到实处，为我区教育发展提供法律保障。坚持以公开为常态，不公开为例外原则，严格落实《政府信息公开条例》，积极推进局机关信息公开工作，建立健全合理的工作机制，在制度上保证了法治政府建设工作的有效落实。近几年按时按要求回复依申请公开申请39宗，在南山政府在线教育局官方网站、南山教育信息网等发布信息6153条，利用“南山教育”微信公众号发布信息838条。
        </p>

        <p>
          二是加强领导干部和机关人员学法用法。局领导班子结合“不忘初心、牢记使命”主题教育活动、理论中心组学习，对《宪法》《中国共产党纪律处理条例》《民办教育促进法》《义务教育法》《民法典》等法律进行专题学习；组织机关工作人员参加法制专题讲座、法治研讨等各种教育培训。通过一系列的学习培训，全局干部进一步掌握法治政府的相关内容，牢固树立了为民服务、便民服务的思想意识，进一步提高工作效率和服务质量。
        </p>
        <p>
          三是加强对行政执法人员的教育管理。根据上级有关要求组织行政执法人员参加相关法律知识培训及考试，提高行政执法人员证件管理的规范化和信息化水平。
        </p>
        <p>
          二）建章立制，优化教育行政执法水平
        </p>
        <p>
          一是建立法律顾问制度，全系统100%聘请常年法律顾问，推进教育法治建设，规范依法治教、依法行政，维护师生合法权益、打造和谐幸福教育。
        </p>
        <p>
          二是做好政府合同合法性审查工作，规范行政机关合同管理，防范政府合同法律风险。认真做好行政执法案卷自查工作，发现问题一起，改进一起，同时落实案卷归档专人负责，提高案卷管理质量。
        </p>
        <p>
          三是制定印发《南山区教育局行政执法公示信息内部审核制度》《南山区教育局行政执法音像记录设备使用管理制度》《南山区教育局重大行政执法决定法制审核制度》等三项制度，严格依法开展行政审批等行政执法工作。实现100%行政审批网上受理，提升行政审批效能。加强与工商、公安、街道等部门协同，开展校外培训机构治理等联合执法工作。
        </p>
        <p>
          四是积极开展“双随机一公开”工作。制定了《南山区教育局“双随机一公开”工作细则》《南山区教育局随机抽查事项清单》，建立教育执法人员名录库及检查对象名录库。精心设计检查记录表，检查后及时做好信息录入和结果公布。近年开展全区中小学及幼儿园等教育单位和民办教育机构检查90次，抽查单位数达90所。
        </p>
        <p>
          （三）廉洁自律，不断落实依法治教
        </p>
        <p>
          围绕廉政风险防控机制建设，进一步把师德师风建设放在更加突出的位置，把廉洁行政、廉洁从教的要求贯穿于各个环节。
        </p>
        <p>
          一是推进学校校长违纪违法问题专项整治，突出加强对“一把手”的监督。认真落实校（园）长队伍廉政培训工作，突出“头雁”效应。通过党风廉政教育培训，要求校（园）长们要切实认清形势，对标先进，发现工作中存在的问题，提高站位，学好政策，真正把抓好全面从严治党和党风廉政建设作为校园长足发展和教育教学改革创新的政治保证，把握方向、带好队伍，当好“领头雁”和“掌舵人”。充分认清“四风”问题的顽固性和反复性，严格落实主体责任。下发《关于做好寒假春节期间有关工作的通知》、《关于转发区纪委纠正
          “四风”相关工作的重要通知》等多个通知，并在全区基层支部书记述职述廉会上进行强调，要求各单位严格落实主体责任，将纠正“四风”，强化日常监督管理，不断巩固和落实中央八项规定精神的成果纳入到本系统党风廉政建设重点工作之中。瞄准重点精准监督，有效防控校园廉政高风险点。建立“阳光食堂”食材供应企业预选库并投入运行；改革公办学校采购制度，设立物料采购中心。

        </p>
        <p>
          二是高度重视师德师风问题，持续加强师德师风监管力度。落实监督责任推动主体责任落实，确保教师队伍建设的正确政治方向。全市率先制定了《南山区教育系统校园廉政风险防控工作实施方案》，构建起预警在先、防范在前的工作机制，打造教育系统风清气正的育人环境。坚持“惩防并举”，抓好重点部位、关键岗位人员廉洁从业教育。严肃查处违反师德“十条红线”的信访举报件，深化师德师风综合治理。用活用好监督执纪“四种形态”，保持纪律审查的高压态势；成立明察暗访工作组，开展专项督导，强化师德师风工作监管；发挥基层监督作用，针对师德建设突出问题开展自查自纠。
        </p>
        <p>
          （四）依法治校，构建和谐校园
        </p>
        <p>
          一是深入开展青少年宪法教育，推动法治教育常态化，将法治教育贯穿于立德树人的全过程。1.全市率先拉开“宪法教育大课堂”法治教育序幕，市司法局、市教育局、团市委、市律协、各区司法局相关负责同志以及全市各普法讲师团成员参加了活动。2.开展“校园法治文化节”“青少年法治教育论坛”“法治南山院校共建暨反校园暴力模拟法庭活动”“检察院开放日”“检察官进校园”等活动，让学生亲身体验，感悟，深刻感受法律的神圣与威严，更感受未成年人法庭教育的力量。3.组织南山区“学宪法讲宪法”演讲比赛，选派学生参加深圳市“中学生‘学宪法、讲宪法法治故事演讲’”比赛，获奖数量位居全市之首。4.邀请区政协委员进校园，建立多方联动机制，搭建法治育人平台，实现社会资源共享共建，助力广大中小学生健康成长。5.发挥家长资源，形成教育合力，邀请律师家长到学校为学生开展“律师进课堂”普法公益活动，组成“学宪法”宣传小队，宣讲宪法小故事。6.加强普法宣传。每次普法活动，做到“校校有报道，月月有总结”。先后在南山教育微信公众号、《蛇口消息报》发布了150多篇宣传报道，营造学习宪法、宣传宪法的良好社会氛围。
        </p>
        <p>
          二是开展“12.4”国家宪法日系列活动，厚植爱国主义情怀。每年举行“12·4”国家宪法日系列宣传活动，对本年度教育系统开展的法治教育作总结与展示；开展“宪法小卫士”全员在线学习活动，全区约140000学生在线学习宪法知识，组织参加全国宪法小卫士竞赛；各校同步开展国家宪法日“宪法晨读”活动；举办“南山区青少年法治教育文化节”，通过一系列活动，在广大青少年心中筑牢宪法精神，为推进依法治国、建设社会主义法治国家，做出应有的贡献。
        </p>
        <p>
          （五）提升教育治理能力，创建世界一流教育
        </p>
        <p>
          一是加强“创文”工作与校外培训机构长效治理。用好黑白名单制度，联合各方资源和力量，建立培训机构等规范化管理的长效机制。规范民办教育办学行为，支持优质民办学校发展。
        </p>
        <p>
          二是高标准打造省维稳示范点。优化研判机制，提高矛盾多元化解协调能力，完善舆情监测、分析、报送工作强化制度建设、规范信访处置，提高信访维稳信息系统应用水平。
        </p>
        <p>
          三是营造安全和谐育人环境。落实“健康第一”，强化心理健康教育，建立学校-社区-家庭三方工作联动体制。压实校园安全主体责任，加强安全隐患排查整治，增强师生安全意识和技能。完善校园周边综合整治机制，加强部门联动，确保校园及周边平安稳定。
        </p>
        <p>
          四是增强教育督导效能。坚持“一校一案一策”，指导各中小学校积极整改。做好政府履职评估工作，迎接幼儿园专项督导评估。推进2020年督学换届选举，不断提升督学队伍专业化水平。
        </p>
        <p>
          二、下一步工作目标
        </p>
        <p>
          （一）开展依法行政机制推进行动
        </p>
        <p>
          依法进一步明确职能权限与责任，优化权力清单、责任清单。开展行政职权梳理和标准化建设，实现教育系统行政管理制度化、规范化。切实按照法定职责必须为、法无授权不可为的原则，依法清理、精简行政权力，重点梳理在行政许可、行政处罚、学校管理等方面的职责。进一步推进教育行政审批制度改革，提升行政审批效能。
        </p>
        <p>
          （二）施行教育行政执法体制改革行动
        </p>
        <p>
          健全教育行政执法专项检查制度，对各类学校依法办学情况进行执法检查。完善对社会违法办学情况的专项检查制度，依法建立健全“双随机”为主的事中事后监管机制，督促、查处相关教育违法违规办学行为。完善教育行政部门与外部门间的横向协同执法机制。
        </p>
        <p>
          （三）全面推进教育领域信息公开行动
        </p>
        <p>
          坚持以公开为常态、不公开为例外原则，严格落实《政府信息公开条例》，积极推进教育行政部门、学校的决策、执行、管理、服务的信息公开，探索信息公开的新途径、新方式。指导、监督学校全面履行信息公开义务。
        </p>
        <p>
          （四）落实依法治校科学发展行动
        </p>
        <p>
          深入推进现代学校制度建设，建立健全学校章程核准后的执行和监督评价机制，推动学校建立以章程为核心的规章制度体系。继续推进依法治校示范学校创建工作，创建一批省、市级依法治校示范校，推广典型经验，发挥示范校的引领作用，提高依法治校整体水平；探索建立学校依法治校年度报告制度。
        </p>
        <p>
          （五）完善法治教育创新提升行动
        </p>
        <p>
          继续认真落实“法治南山教育”工作，落实《青少年法治教育大纲》，深入开展校园法治建设，继续组织国家宪法日和相关纪念日学生法治宣传活动，依托党团、少先队等组织，开展形式多样、奋发向上的青少年学生法治宣传教育活动。加强法治教育实践基地建设，加强宪法教育，多种形式普及法律知识，开展法治实践教育，培养学生的法治意识和素养。推动媒体承担法治宣传教育职责，健全与媒体协同公益普法机制，依托广播、报纸、电台、电视台等传统媒体及运用手机短信、微博、微信、户外电子屏等新型宣传阵地开展普法活动，使法治宣传教育贴近学生、贴近实际、入脑入心。
        </p>
