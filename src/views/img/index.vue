      <h3>南山区人力资源局“七五” 普法工作自评报告</h3>
      <span>2020-08-26 17:46来源</span>
      <i>南山区人力资源局</i>
      <div>
        <p>
          《中共中央国务院关于支持深圳建设中国特色社会主义先行示范区的意见》明确提出了“法治城市示范”的战略定位，要求加强“法治政府建设，完善重大行政决策程序制度，提升政府依法行政能力。加大全面普法力度，营造尊法学法守法用法的社会风尚。”根据市、区普法办关于做好“七五”普法规划及相关文件要求，积极采取有效措施，通过各种方式，利用各种渠道，抓重点，促和谐，大力做好全区人力资源普法宣传教育工作，取得了好的成效。
        </p>
        <p>
          一、全区人力资源普法宣传教育法治水平有新提升。
        </p>
